function courseBitcoin(dollars, bitcoin) {
    return dollars / bitcoin;
};

console.log(courseBitcoin(100, 5));


function howMuchDollars(bitcoin, course) {
    return bitcoin * course;
};

console.log(howMuchDollars(10, 5));

function iCanBuyBTC(dollars, course) {
    return dollars / course;
};

console.log(iCanBuyBTC(10, 5));